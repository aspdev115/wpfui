namespace WPFUI.Theme
{
    /// <summary>
    /// Collection of default themes available for Windows 11 (version 10.2022).
    /// </summary>
    public enum Style
    {
        /// <summary>
        /// Unknown system style.
        /// </summary>
        Unknown,

        /// <summary>
        /// Default light theme.
        /// </summary>
        Light,

        /// <summary>
        /// Default dark theme.
        /// </summary>
        Dark,

        /// <summary>
        /// First custom, kinda purple Windows 11 theme.
        /// </summary>
        Glow,

        /// <summary>
        /// Second custom, kinda red Windows 11 theme.
        /// </summary>
        CapturedMotion,

        /// <summary>
        /// Third custom, kinda washed off cyan Windows 11 theme.
        /// </summary>
        Sunrise,

        /// <summary>
        /// Fourth custom, kinda gray Windows 11 theme.
        /// </summary>
        Flow
    }
}
