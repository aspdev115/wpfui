        /// <summary>
        /// Converts <see cref="MiconIcon"/> to <see langword="char"/> based on the ID, if <see langword="null"/> or error, returns <see cref="MiconGlyph.Accept"/>
        /// </summary>
        public static char ToGlyph(Common.MiconIcon? icon)
        {
            if (icon == null)
                return MiconGlyph.Accept;

            char? character = typeof(MiconGlyph).GetField(icon.ToString()).GetValue(null) as char?;

            if (character == null)
                return MiconGlyph.Accept;
            else
                return (char)character;
        }

        /// <summary>
        /// Converts <see cref="MiconIcon"/> to <see langword="string"/> based on the ID, if <see langword="null"/> or error, returns <see cref="MiconGlyph.Accept"/>
        /// </summary>
        public static string ToString(Common.MiconIcon? icon)
        {
            return MiconGlyph.ToGlyph(icon).ToString();
        }
